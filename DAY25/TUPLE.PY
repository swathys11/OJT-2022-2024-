# Joining/ Concatenation 
# The + operator, concatenation operator is used to joining two tuples
# eg 1
tuplel = (1, 2, 3)
tuple2 =(4,5,3,6)
print (tuplel + tuple2)

# eg 2
tuplel = (1,2,3)
tuple2 =(4,5,6)
tuple3 =('a', 12.5, (7,8))
print (tuplel + tuple2 + tuple3)


# Repeating/ Replicate 
# You can use * operator to replicate a tuple a specified number of times

tuplel =(1,2,3)
print(tuplel *4)

